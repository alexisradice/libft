#include "libft.h"

void	*ft_memset(void *pointer, int value, size_t count)
{
	size_t			i;
	unsigned char	*p;

	i = 0;
	p = pointer;
	while (i < count)
	{
		*p = value;
		p++;
		i++;
	}
	return (pointer);
}
