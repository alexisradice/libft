#include "libft.h"

int	ft_isprint(int c)
{
	if (c == ' ')
		return (64);
	if ((c >= '!' && c <= '/') || (c >= ':' && c <= '@'))
		return (16);
	else if ((c >= '[' && c <= '`') || (c >= '{' && c <= '~'))
		return (16);
	else if (c >= 'a' && c <= 'z')
		return (2);
	else if (c >= 'A' && c <= 'Z')
		return (1);
	else if (c >= '0' && c <= '9')
		return (4);
	else
		return (0);
}
